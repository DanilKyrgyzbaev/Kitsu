package kg.geekstudio.data.model.anime

import kg.geekstudio.data.maper.DataMapper
import kg.geekstudio.domain.model.anime.AnimeAttributes

data class AnimeAttributesDto(
    val createdAt: String,
    val updatedAt: String,
    val slug: String,
    val synopsis: String,
    val description: String,
    val coverImageTopOffset: Int,
    val titles: AnimeTitlesDto,
    val canonicalTitle: String,
    val abbreviatedTitles: List<String>,
    val averageRating: String,
    val ratingFrequencies: Map<String, String>,
    val userCount: Int,
    val favoritesCount: Int,
    val startDate: String,
    val endDate: String,
    val nextRelease: String?,
    val popularityRank: Int,
    val ratingRank: Int,
    val ageRating: String,
    val ageRatingGuide: String,
    val subtype: String,
    val status: String,
    val tba: String?,
    val posterImage: AnimeImageDto,
    val coverImage: AnimeImageDto,
    val episodeCount: Int,
    val episodeLength: Int,
    val totalLength: Int,
    val youtubeVideoId: String,
    val showType: String,
    val nsfw: Boolean
) : DataMapper<AnimeAttributes> {
    override fun toDomain() = AnimeAttributes(
        createdAt,
        updatedAt,
        slug,
        synopsis,
        description,
        coverImageTopOffset,
        titles.toDomain(),
        canonicalTitle,
        abbreviatedTitles,
        averageRating,
        ratingFrequencies,
        userCount,
        favoritesCount,
        startDate,
        endDate,
        nextRelease,
        popularityRank,
        ratingRank,
        ageRating,
        ageRatingGuide,
        subtype,
        status,
        tba,
        posterImage.toDomain(),
        coverImage.toDomain(),
        episodeCount,
        episodeLength,
        totalLength,
        youtubeVideoId,
        showType,
        nsfw

    )
}